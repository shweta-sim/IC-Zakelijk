<script type="text/javascript">
$(document).ready(function(){

var owl = $('#home-carousel');
owl.owlCarousel({
  margin:0,
  loop:true,
  items:1,  
  lazyLoad: true,
  autoplay: false,
  dots: false,
  autoplayTimeout: 6000,
  autoplaySpeed: 1000,
  navSpeed:1000,
  dotsSpeed: 1000,
  autoplayHoverPause: false,
  mouseDrag: false,
  nav: true,
  onDragged: callback,
  onChanged: callback,
  animateIn: 'slideInUp',
  animateOut: 'slideOutUp',
  
});


var owl = $('#testimonial-carousel');
owl.owlCarousel({
  loop:true,
  autoplay: true,
    margin:30,
    items:3, 
    nav:false,
    responsive:{
        0:{
            items:1
        },
        767:{
            items:2
        },
        1024:{
            items:3
        }
    }
});

function callback(event){
  var item = event.item.index;
  $('.slide-item.active').removeClass('active');
  $('#slide_'+item).addClass('active');
  console.log('Current Slide: '+ item);
}

$('.slide-item').click(function(event){
  var i = $(this).data('i');
  owl.trigger('to.owl.carousel', i);
  console.log('Trigger slide: '+ i);
});


});
</script> 

<!-- Scroll Down/Up JavaScript --> 
<script type="text/javascript">
// Scroll down
$(function() {
  $('.arrows a[href*=#]').on('click', function(e) {
    e.preventDefault();
    $('html, body').animate({ scrollTop: $($(this).attr('href')).offset().top}, 500, 'linear');
 });
  
 //Scroll Up
$(document).ready(function () {
    $(window).scroll(function () {
        if ($(this).scrollTop() > 100) {
            $('.arrow-up').fadeIn();
        } else {
            $('.arrow-up').fadeOut();
        }
    });
    $('.arrow-up').click(function () {
        $("html, body").animate({
            scrollTop: 0
        }, 600);
        return false;
    });

}); 
  
});

</script>
